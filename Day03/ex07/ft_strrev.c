char	*ft_strrev(char *str)
{
	int		j;
	int		i;
	char	temp;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		i--;
		j--;
	}
	return (str);
}
