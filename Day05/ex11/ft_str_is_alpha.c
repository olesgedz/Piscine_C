int		ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
	{
		return (0);
	}
	while (str[i] != '\0')
	{
		if (!(('A' <= str[i] && str[i] <= 'Z') ||
			('a' <= str[i] && str[i] <= 'z')))
		{
			return (1);
		}
		i++;
	}
	return (0);
}
